// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
    let index = 0;
    if (num1 > num2){
      index = num2;
    }else if (num1 < num2){
      index = num1;
    }
    return index;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  if (n % 2 === 0){
    return true;
  }
  return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let count = 0;
  for (let i = 0; i < string.length; i++)
    if(string[i] === letter){
      count++;
    }
    return count;
}

